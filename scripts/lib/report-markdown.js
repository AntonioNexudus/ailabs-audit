const { TODAY, TODAY_STR } = require('./config');
const { classifyError, ERROR_HINTS } = require('./util');
const { CHECK_DEFS } = require('./check-defs');

// ---------------------------------------------------------------------------
// Markdown report builder — full technical report with tables, fix commands,
// and raw JSON data block. This is what the AI reads after user consent.
// ---------------------------------------------------------------------------

function buildReport(results, ranDefs, scopeMeta) {
  const lines = [];
  const w = (s) => lines.push(s);
  const defsForReport = Array.isArray(ranDefs) && ranDefs.length > 0 ? ranDefs : CHECK_DEFS;

  w(`# Nexudus Account Health Audit — ${TODAY_STR}`);
  w('');
  w(`**Generated:** ${TODAY.toISOString()}`);
  if (scopeMeta) {
    const bizScope = scopeMeta.businesses
      ? `Business IDs ${scopeMeta.businesses.join(', ')}`
      : 'all businesses';
    w(`**Scope:** ${bizScope} · ${scopeMeta.level} (${scopeMeta.checksRun.length} of ${CHECK_DEFS.length} checks: #${scopeMeta.checksRun.join(', #')})`);
    if (scopeMeta.coworkerStats) {
      const s = scopeMeta.coworkerStats;
      w(`**Coworkers:** ${s.total} total · ${s.members} Members (active contract) · ${s.contacts} Contacts (no active contract)`);
    }
  }
  w('');

  const severityDefs = defsForReport.filter((d) => !d.section || d.section === 'severity');
  const insightDefs = defsForReport.filter((d) => d.section === 'insights');

  // Executive summary (severity checks only)
  w('## Executive Summary');
  w('');
  w('| # | Check | Severity | Issues |');
  w('|---|-------|----------|--------|');

  let total = 0;
  for (const def of severityDefs) {
    const r = results[def.key];
    if (!r) continue;
    const count = r.status === 'ERROR' ? 'ERROR' : r.items.length;
    if (typeof count === 'number') total += count;
    w(`| ${def.num} | ${def.name} | ${def.severity} | ${count} |`);
  }

  w('');
  w(`**Total issues: ${total}**`);
  w('');
  w('---');
  w('');

  // Detail sections (severity checks)
  for (const def of severityDefs) {
    const r = results[def.key];
    if (!r) continue;

    w(`## ${def.num}. ${def.heading} [${def.severity}]`);
    w('');

    if (r.status === 'ERROR') {
      const cls = r.errorClass || classifyError({ message: r.error });
      w(`> ERROR \`[${cls}]\`: ${r.error}`);
      w(`> *Hint:* ${ERROR_HINTS[cls] || ERROR_HINTS.unknown}`);
    } else if (r.items.length === 0) {
      w('> No issues found.');
    } else {
      w(`> ${def.description(r.items.length)}`);
      w('');
      w(`| ${def.columns.join(' | ')} |`);
      w(`|${def.columns.map(() => '---').join('|')}|`);
      for (const item of r.items) {
        const cells = def.row(item);
        w(`| ${cells.join(' | ')} |`);
      }
    }

    w('');
    w('---');
    w('');
  }

  // Insights section
  if (insightDefs.length > 0) {
    w('## Insights');
    w('');

    for (const def of insightDefs) {
      const r = results[def.key];
      if (!r) continue;

      w(`### ${def.heading}`);
      w('');

      if (r.status === 'ERROR') {
        const cls = r.errorClass || classifyError({ message: r.error });
        w(`> ERROR \`[${cls}]\`: ${r.error}`);
        w(`> *Hint:* ${ERROR_HINTS[cls] || ERROR_HINTS.unknown}`);
      } else if (r.items.length === 0) {
        w('> Nothing to report.');
      } else {
        w(`> ${def.description(r.items.length)}`);
        w('');
        w(`| ${def.columns.join(' | ')} |`);
        w(`|${def.columns.map(() => '---').join('|')}|`);
        for (const item of r.items) {
          const cells = def.row(item);
          w(`| ${cells.join(' | ')} |`);
        }
      }

      w('');
    }

    w('---');
    w('');
  }

  // Raw JSON data block with the full structured results. The AI assistant
  // parses this to build fix commands.
  w('<details>');
  w('<summary>Raw JSON audit data (for programmatic use)</summary>');
  w('');
  w('```json');
  w(JSON.stringify(results, null, 2));
  w('```');
  w('');
  w('</details>');
  w('');
  w(`*Generated by nexudus-audit skill on ${TODAY.toISOString()}. Run \`node skills/nexudus-audit/scripts/audit.js\` to refresh.*`);

  return lines.join('\n');
}

module.exports = { buildReport };
